export function build(ctx) {
  const flights = ctx.requiredInput("layout.flights", {
    code: "stair-flight-layout-missing",
    message: "Solved flight layout is required to generate flight metadata."
  });
  if (!Array.isArray(flights)) return;
  ctx.objectPattern.create("flightPattern", {
    type: "path-pattern",
    generatedObjectIds: [],
    transform: {
      kind: "stair-flight-layout",
      flights
    },
    notes: "Solved flight layout metadata. Physical parts are generated by tread, support, railing, and connection child components."
  });
  ctx.output("flights", flights);
}
